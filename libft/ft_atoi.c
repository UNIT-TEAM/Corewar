#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int m;
	int res;

	i = 0;
	m = 1;
	res = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' ||
		str[i] == '\r' || str[i] == '\f' || str[i] == '\v')
		i++;
	if (str[i] == '-')
		m = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res *= 10;
		res = res + m * (str[i] - 48);
		i++;
	}
	return (res);
}
